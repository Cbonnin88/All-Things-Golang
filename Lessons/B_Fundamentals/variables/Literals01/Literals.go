package main

import "fmt"

func main() {
	fmt.Println(100 + 200)
	fmt.Println(2000 - 200)
	fmt.Println(100 * 200)
	fmt.Println(9 / 7)
}

// Literals are values written into code, they are what they literally say
/* Arithmetic values in Go:
+ is to add
- is to subtract
* is to multiply
/ is to take the remainder between to numbers (the modulus operator)
*/
