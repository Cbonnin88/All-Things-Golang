package main

import "fmt"

func main() {
	fmt.Println("true && true will give us: ", true && true)
	fmt.Println("true && false will give us: ", true && false)
	fmt.Println("true || true will give us: ", true || true)
	fmt.Println("true || false will give us: ", true || false)
	fmt.Println("!true will give us: ", !true)

}
